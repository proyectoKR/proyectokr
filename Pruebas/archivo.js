//operadores de asignacion, incremento y decremento

let a = 1;

document.write('valor de a: ', a);
document.write('<br>');
a++;//aumenta valor en 1
document.write('valor de a: ', a);
document.write('<br>');
a+=5;//aumento variable
document.write('valor de a: ',a);
document.write('<br>');
a--;// resta valor en 1
document.write('valor de a: ',a);
document.write('<br> ');
a-=3;// resta variable
document.write('valor de a: ',a);
document.write('<br> ');
a*=2;//a*a
document.write(' valor de a: ',a);
document.write('<br> ');
a/=3;//a/a
document.write('valor de a:  ',a);
document.write('<br> ');
a**=2;// elevar la potencia
document.write('valor de a: ',a);
